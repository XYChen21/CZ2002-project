package projectoop;

public class Food {

	private String name;
	private int index;
	private double price;

	public String getName()
	{return name;}

	public double getPrice()
	{return price;}

	public int getIndex()
	{return index;}

	public void setIndex()
	{this.index = index;}

	public void setName()
	{this.name = name;}
	
	public void setPrice()
	{this.price = price;}

}
